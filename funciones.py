#Tema Funciones

#Donde se declara la funcion y recibe 2 parametros
def resta(a, b):
    #devuelve la resta de los parametros
    return a - b

def suma(a, b):
    #devuelve la suma de los parametros
    return a + b

def multiplicar(a, b):
    #devuelve la multiplicacion de los parametros
    return a * b
